class Person

  # Setter (writer) for persons name
  def name= (persons_name)
    @name = persons_name
  end

  # Getter (reader) for persons name
  def name
    @name
  end

  # Setter (writer) for the job of the person
  def job= (work)
    @job = work
  end

  # Getter (reader) for the job of the person
  def job
    @job
  end


end